package com.sk;

public class student {
	
	private int sid;
	private String sname;
	private int sage;
	private double smarks;
	private String squl;
	
	
	public int getSid() {
		return sid;
	}
	public void setSid(int sid) {
		this.sid = sid;
	}
	public String getSname() {
		return sname;
	}
	public void setSname(String sname) {
		this.sname = sname;
	}
	public int getSage() {
		return sage;
	}
	public void setSage(int sage) {
		this.sage = sage;
	}
	public double getSmarks() {
		return smarks;
	}
	public void setSmarks(double smarks) {
		this.smarks = smarks;
	}
	public String getSqul() {
		return squl;
	}
	public void setSqul(String squl) {
		this.squl = squl;
	}
	
	

}
